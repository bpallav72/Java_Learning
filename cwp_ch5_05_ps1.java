package com.company;

/* write a program to print the following pattern
****
***
**
*
   */
public class cwp_ch5_05_ps1 {
    public static void main(String[] args) {
        for(int i=4;i>0;i--){
            for(int j=0;j<i;j++){
                System.out.print("*");
            }
            System.out.print("\n");
        }


    }
}
